// Primary varicode string for character c (0..255); used by the generator.
static inline const char* thor_varienc(int c, int secondary) {
    if (!secondary) { return MFSK_VARIENC[(c >= 0 && c < 256) ? c : 0]; }
    if (c >= ' ' && c <= 'z') { return THOR_SEC_VARIENC[c - ' ']; }
    return MFSK_VARIENC[0];
}

// Decode an accumulated varicode symbol. Returns the character (0..255),
// or a secondary character flagged with 0x100, or -1 if not found.
static inline int thor_varidec(unsigned int symbol) {
    if (symbol < 0xB80) {
        for (int i = 0; i < 256; i++) if (symbol == MFSK_VARIDEC[i]) return i;
        return -1;
    }
    for (int i = 0; i < 91; i++) if (symbol == THOR_SEC_VARIDEC[i]) return (int)(0x20 + i + 0x100);
    return -1;
}

